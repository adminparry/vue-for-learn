## 5年前端开发的感悟
### 首先是对语言的感悟
计算机语言就拿JavaScript来说 对应着JavaScript的解释器 那么无论是脚本语言还是别的什么语言都是对人类和计算机世界的一个映射关系 脚本语言则更是划分领域之后的便捷语言
例如js就是浏览器这个领域的语言一样 语言的设计大致分为方法和数据结构或者叫结构体不过大同小异 还有程序流程控制语句
当然解释器解释也有通常意义上的作用域 对于浏览器来说一句话呈现文档
浏览器可以阅读很多类型的文档取决于发送给浏览器的mimeType类型
而浏览器对于处理html类型有着更多的支持
也定义了很多可以数过来的标签 对应标签的使用方式来呈现和使用
而css和js则是对html的补充一个是样式一个是行为
