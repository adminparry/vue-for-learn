css定义了书写规则去更进一步去方便对html的解释
或许你听过文档流一词 这就需要你对css的掌握程度
记得早期的时候有着更好的工资就是对css的hack
因为每个浏览器的解释方式不同所以导致在不同浏览器展现形式不太一样
